// https://www.alphavantage.co/query?function=TIME_SERIES_DAILY&symbol=IBM&apikey=demo
//
function stock_data(symbol) {
    return new Promise(async function (resolve, reject) {
        const use_cache = window.APP.use_cache ?? false;
        try {
            let series;
            if (!use_cache) {
                series = await fetch_api_data(symbol);
            } else {
                series = await fetch_cached_data(symbol);
            }
            // console.log(symbol, series);

            if (!series) {
                reject(`Not Found: ${symbol}`);
                return;
            }

            resolve(series);
        } catch (e) {
            console.log('Error fetching:', e)
            reject(e.toString());
        }
    })
}

function fetch_api_data(symbol) {
    console.log('fetch_api_data', symbol);
    return new Promise(async function (resolve, reject) {

        try {
            // Get data from the API
            //
            symbol = 'IBM';   // override to allow their demo API key to work
            const api_key = 'demo';
            // const api_key = 'KPXUYHVL58QXW86D';

            const response = await fetch(`https://www.alphavantage.co/query?function=TIME_SERIES_DAILY&symbol=${symbol}&apikey=${api_key}`);
            if (!response.ok) {
                reject(`HTTP ERROR: ${response.status}`);
                return;
            }
            const data = await response.json();

            const series = data['Time Series (Daily)'];
            if (!series) {
                reject(`Not Found: ${symbol}`);
                return;
            }

            resolve(series);
        } catch (e) {
            console.log('Error fetching:', e)
            reject(e.toString());
        }
    });
}

function fetch_cached_data(symbol) {
    console.log('fetch_cached_data', symbol);

    return new Promise(function (resolve, reject) {
        setTimeout(function () {
            if (!all_stocks.hasOwnProperty(symbol)) {
                reject(`Not Found: ${symbol}`);
                return;
            }

            const data = all_stocks[symbol];

            const series = data['Time Series (Daily)'];
            if (!series) {
                reject(`Not Found: ${symbol}`);
                return;
            }

            resolve(series);
        }, 250);
    });
}

